class Exam6 {
	public static void main (String[] args) {
		System.out.println("Hello,\n world");//줄바꿈은 \
		System.out.println("Hello,\r world");
		System.out.println("Hello, \b world");
		System.out.println("Hello, \t\t\tworld");//띄어쓰기
		System.out.println("Hello, \"w\"orld");// "w"강조
		System.out.println("Hello. 'w'orld");
		System.out.println('\'');//'만 표시
		System.out.println("Hello,\\\\\\world");// \\\나타냄
	
	}
}