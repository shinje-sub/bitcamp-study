class Exam4{
	public static void main(String[] args) {
	   //비교 연산의 결과는 논리값이다.
		System.out.println(4 > 5);
	   System.out.println(4 < 5);
	   
	   //논리 연산의 결과는 논리값이다.
	   System.out.println(true || false);
	   System.out.println(true && false);
	   
	   //작은 따옴표가 알려준 유니코드 값 비교하기
	   System.out.println(65 == 'A');
	   System.out.println(65 == 'B');
	   
	}
}

